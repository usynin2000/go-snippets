package main

import "fmt"


// –ò–∑–º–µ–Ω–µ–Ω–∏–µ –ø–µ—Ä–º–µ–Ω–Ω–æ–π –ø–æ —É–∫–∞–∑–∞—Ç–µ–ª—é
func increment(n *int) {
	*n++
}

// –§—É–∫–Ω—Ü–∏—è, –≤–æ–∑–≤—Ä–∞—â–∞—è —É–∫–∞–∑–∞—Ç–µ–ª—å
func createPointer(val int) *int {
	return &val
}

func main() {
	// 1. –ë–∞–∑–æ–≤—ã–π –ø—Ä–∏–º–µ—Ä
	x := 10
	ptr := &x  // –±–µ—Ä–µ–º –∞–¥—Ä–µ—Å –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π
	fmt.Println("1) x:", x, "| –∞–¥—Ä–µ—Å:", ptr, "| —á–µ—Ä–µ–∑ —É–∫–∞–∑–∞—Ç–µ–ª—å:", *ptr)

	*ptr = 20 // –º–µ–Ω—è–µ–º —á–µ—Ä–µ–∑ —É–∫–∞–∑–∞—Ç–µ–ª—å
	fmt.Println("–Ω–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ x:", x)

	// 2. –£–∫–∞–∑–∞—Ç–µ–ª—å –∫–∞–∫ –∞—Ä–≥—É–º–µ–Ω—Ç —Ñ—É–Ω–∫—Ü–∏–∏
	increment(&x)
	fmt.Println("2) –ø–æ—Å–ª–µ increment:", x)

	// 3. –£–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —É–∫–∞–∑–∞—Ç–µ–ª—å
	p1 := &x
	p2 := &p1

	**p2 = 42
	fmt.Println("3) —á–µ—Ä–µ–∑ –¥–≤–æ–π–Ω–æ–π —É–∫–∞–∑–∞—Ç–µ–ª—å", x)

	// 4. –£–∫–∞–∑–∞—Ç–µ–ª—å, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º—ã–π –∏–∑ —Ñ—É–Ω–∫—Ü–∏–∏
	p3 := createPointer(99)
	fmt.Println("4) createPointer -> ", *p3)

	// 5. Nil-—É–∫–∞–∑–∞—Ç–µ–ª—å
	var p4 *int // –ø–æ–∫–∞ –ø—É—Å—Ç–æ–π
	if p4 == nil {
		fmt.Println("5) p4 –ø—É—Å—Ç–æ–π (nil)")
	}
	y := 7
	p4 = &y
	fmt.Println("  —Ç–µ–ø–µ—Ä—å p4 —É–∫–∞–∑—ã–≤–∞–µ—Ç –Ω–∞:", *p4)

	// 6. –£–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —Å—Ç—Ä—É–∫—Ç—É—Ä—É
	type Person struct {
		Name string
		Age int
	}
	p := Person{Name: "Alex", Age: 24}
	pPtr := &p
	pPtr.Age = 25 // –º–æ–∂–Ω–æ –º–µ–Ω—è—Ç—å –ø–æ–ª—è –±–µ–∑ (*pPtr)
	fmt.Println("6) —Å—Ç—Ä–∫—Ç—É—Ä–∞ —á–µ—Ä–µ–∑ —É–∫–∞–∑—ã–≤–∞—Ç–µ–ª—å:", p)
}


